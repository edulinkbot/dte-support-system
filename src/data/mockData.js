const supportStatuses = ['ปกติ', 'เฝ้าระวัง', 'ต้องติดตาม', 'ได้รับคำปรึกษาแล้ว'];
const studyPlanStatuses = ['ลงทะเบียนตามแผน', 'ต้องปรับแผนรายวิชา', 'รอตรวจสอบหน่วยกิต'];

export const students = cohortTemplates.flatMap((cohort) =>
  Array.from({ length: cohort.count }, (_, index) => {
    const running = String(index + 1).padStart(4, '0');
    const stat = cohort.cohortStat;
    const statusIndex = (index + Number(cohort.year)) % supportStatuses.length;
    const planIndex = (index + Number(cohort.year)) % studyPlanStatuses.length;
    const lifecycleStatus = index < stat.graduated
      ? 'จบการศึกษา'
      : index < stat.graduated + stat.inactive
        ? 'พ้นสภาพ'
        : 'คงอยู่';
    const displayYear = cohort.label;
    const credits = lifecycleStatus === 'จบการศึกษา'
      ? 136
      : Number(cohort.year) === 1 ? 21 : Number(cohort.year) === 2 ? 61 : Number(cohort.year) === 3 ? 97 : Number(cohort.year) === 4 ? 132 : 136;
    return {
      id: `${cohort.prefix}2116${running}`,
      name: `นักศึกษารหัส ${cohort.prefix}-${String(index + 1).padStart(2, '0')}`,
      year: cohort.year,
      displayYear,
      academicYear: cohort.academicYear,
      group: cohort.group,
      advisor: `อาจารย์ที่ปรึกษา${displayYear}`,
      creditsPassed: credits,
      creditsPlan: 136,
      gpa: lifecycleStatus === 'พ้นสภาพ' ? '-' : (2.35 + ((index % 9) * 0.16)).toFixed(2),
      supportStatus: lifecycleStatus === 'พ้นสภาพ' ? 'ต้องติดตาม' : supportStatuses[statusIndex],
      studyPlanStatus: lifecycleStatus === 'จบการศึกษา' ? 'สำเร็จการศึกษาแล้ว' : studyPlanStatuses[planIndex],
      lifecycleStatus,
      note: lifecycleStatus === 'จบการศึกษา'
        ? 'สำเร็จการศึกษาตามข้อมูลทะเบียน'
        : lifecycleStatus === 'พ้นสภาพ'
          ? 'พ้นสภาพตามข้อมูลทะเบียน ใช้เป็นหลักฐานติดตาม cohort'
          : statusIndex === 2 ? 'นัดหมายให้คำปรึกษาและติดตามผล' : 'ติดตามตามรอบปกติ',
    };
  }),
);

